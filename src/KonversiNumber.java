public class KonversiNumber {
    public static void main(String[] args) {
        /*
         * Number DataType Convert
         * Widening Conver = small to large (byte to long);
         * Narrowing Convert = large to small (long to byte)
         */

         // Widening (Otomatis)
         byte iniByte = 10;
         short iniShort = iniByte;
         int iniInt = iniShort;

         // Caution of number overflow if convert from large to small!
    }
}
