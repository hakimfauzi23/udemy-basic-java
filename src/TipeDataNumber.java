public class TipeDataNumber {
    public static void main(String[] args) {

        /* Integer Number */
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 100000000;
        long iniLong = 100000000L;

        /* Floating Number */
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        /* Literals Number */
        int decimalInt = 34;
        int hexaDecimal = 0xFFFFF;
        int binaryDecimal = 0b1010100;

        /* If the number is big, we can use underscore to separate digits */
        int amount = 1000_000_000;
    }
}
